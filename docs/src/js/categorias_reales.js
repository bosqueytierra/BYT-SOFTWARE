// ===== FUNCIONES DE UTILIDAD =====
function obtenerCategorias() {
    return Object.keys(categoriasMateriales);
}

function obtenerMaterialesPorCategoria(categoria) {
    return categoriasMateriales[categoria] || [];
}

function agregarCategoria(nombreCategoria) {
    if (!categoriasMateriales[nombreCategoria]) {
        categoriasMateriales[nombreCategoria] = [];
        return true;
    }
    return false;
}

function eliminarCategoria(nombreCategoria) {
    if (categoriasMateriales[nombreCategoria]) {
        delete categoriasMateriales[nombreCategoria];
        return true;
    }
    return false;
}

function agregarMaterial(categoria, nombreMaterial) {
    if (categoriasMateriales[categoria] && !categoriasMateriales[categoria].includes(nombreMaterial)) {
        categoriasMateriales[categoria].push(nombreMaterial);
        return true;
    }
    return false;
}

function eliminarMaterial(categoria, nombreMaterial) {
    if (categoriasMateriales[categoria]) {
        const index = categoriasMateriales[categoria].indexOf(nombreMaterial);
        if (index > -1) {
            categoriasMateriales[categoria].splice(index, 1);
            return true;
        }
    }
    return false;
}

function editarMaterial(categoria, nombreActual, nuevoNombre) {
    if (categoriasMateriales[categoria]) {
        const index = categoriasMateriales[categoria].indexOf(nombreActual);
        if (index > -1 && !categoriasMateriales[categoria].includes(nuevoNombre)) {
            categoriasMateriales[categoria][index] = nuevoNombre;
            return true;
        }
    }
    return false;
}

// Hacer disponibles las variables globalmente
if (typeof window !== 'undefined') {
    window.categoriasMateriales = categoriasMateriales;
    window.obtenerCategorias = obtenerCategorias;
    window.obtenerMaterialesPorCategoria = obtenerMaterialesPorCategoria;
    window.agregarCategoria = agregarCategoria;
    window.eliminarCategoria = eliminarCategoria;
    window.agregarMaterial = agregarMaterial;
    window.eliminarMaterial = eliminarMaterial;
    window.editarMaterial = editarMaterial;
}